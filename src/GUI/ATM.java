    //Checks what kind of transaction if currently being done and implements it and calls its panel.
    public void transaction(Account account){
        //Withdraw
        if(this.tempTransaction.equals("withdraw")){      
            if(this.tempValue <= 0){
                JOptionPane.showMessageDialog(null, "Sorry, withdrawal should be greater than 0.");
                amountText.setText("");
                accountsPanel.disable();
                accountsPanel.setVisible(false);
                withdrawPanel.enable();
                withdrawPanel.setVisible(true);
            }
            else if(account.withdraw(this.tempValue) == 0){
                this.tempValue = 0;
                this.tempTransaction = "";
                amountText.setText("");
                accountsPanel.disable();
                accountsPanel.setVisible(false);
                continuePanel.enable();
                continuePanel.setVisible(true);
                
            }
            else{
                amountText.setText("");
                accountsPanel.disable();
                accountsPanel.setVisible(false);
                withdrawPanel.enable();
                withdrawPanel.setVisible(true);
            }
        } 
        //Deposit
        else if(this.tempTransaction.equals("deposit")){
            //check if 0 or negative value
            if(this.tempValue <= 0){
                JOptionPane.showMessageDialog(null, "Sorry, deposit should be greater than 0.");
                depositTextField.setText("");
                accountsPanel.disable();
                accountsPanel.setVisible(false);
                depositPanel.enable();
                depositPanel.setVisible(true);
            }
            else{
                account.deposit(this.tempValue);                
                JOptionPane.showMessageDialog(null, "Deposit successful done!");
                this.tempValue = 0;
                this.tempTransaction = "";
                depositTextField.setText("");
                accountsPanel.disable();
                accountsPanel.setVisible(false);
                continuePanel.enable();
                continuePanel.setVisible(true);
                
            }
        }
        //Balance
        else{
            balanceDisplay.setText(Float.toString(account.getAccBalance()));
            accNameDisplay.setText(account.getAccName());
            accNumberDisplay.setText(Integer.toString(account.getAccNumber()));            
            if(tempAccount.equals("savings")){
                setLimitButton.enable();
                setLimitButton.setVisible(true);
                accTypeDisplay.setText("Savings");  
                accDailyLimit.setText("$"+savings.getDailyWithdrawLimit());
            }else if(tempAccount.equals("cheque")){
                accTypeDisplay.setText("Cheque");
                accDailyLimit.setText("none");
            }else if(tempAccount.equals("net")){
                accTypeDisplay.setText("Net-Saver");
                accDailyLimit.setText("$"+net.getDailyWithdrawalLimit());
            }else{
                accTypeDisplay.setText("Fixed");
                accDailyLimit.setText("none");
            }
            accountsPanel.disable();
            accountsPanel.setVisible(false);
            balancePanel.enable();
            balancePanel.setVisible(true);
            
        }
    }
    
