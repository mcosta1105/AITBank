    //Cancel button
    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        if(withdrawPanel.isEnabled()){
            withdrawPanel.disable();
            withdrawPanel.setVisible(false);
            homePanel.enable();
            homePanel.setVisible(true);
        }
        else if(accountsPanel.isEnabled()){
            accountsPanel.disable();
            accountsPanel.setVisible(false);
            homePanel.enable();
            homePanel.setVisible(true);
        }
        else if(depositPanel.isEnabled()){
            depositPanel.disable();
            depositPanel.setVisible(false);
            homePanel.enable();
            homePanel.setVisible(true);
        }
        else if(balancePanel.isEnabled()){
            balancePanel.disable();
            balancePanel.setVisible(false);
            homePanel.enable();
            homePanel.setVisible(true);
            setLimitButton.disable();
            setLimitButton.setVisible(false);
        }
        else if(setLimitPanel.isEnabled()){
            setLimitPanel.disable();
            setLimitPanel.setVisible(false);
            balancePanel.enable();
            balancePanel.setVisible(true);
            limitTextField.setText("");
        }
        else{
            helpPanel.disable();
            helpPanel.setVisible(false);
            homePanel.enable();
            homePanel.setVisible(true);
        }            
        amountText.setText("");
        depositTextField.setText("");
        
    }//GEN-LAST:event_cancelButtonActionPerformed

