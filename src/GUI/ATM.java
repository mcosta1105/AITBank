    //Enter button
    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
        if(!homePanel.isEnabled()){
                if(withdrawPanel.isEnabled()){
                    try{
                        tempValue = Float.parseFloat(amountText.getText());                        
                        withdrawPanel.disable();
                        withdrawPanel.setVisible(false);                    
                        accountsPanel.enable();
                        accountsPanel.setVisible(true);
                    }
                    catch(Exception ex){
                        JOptionPane.showMessageDialog(null, "Please, enter a valid withdraw value.");
                        amountText.setText("");
                    }
                }
                else if(depositPanel.isEnabled()){
                    try{
                        tempValue = Float.parseFloat(depositTextField.getText());
                        depositPanel.disable();
                        depositPanel.setVisible(false);
                        accountsPanel.enable();
                        accountsPanel.setVisible(true);
                    }
                    catch(Exception ex){
                        JOptionPane.showMessageDialog(null, "Please, enter a valid deposit value.");
                        depositTextField.setText("");
                    }                                      
                }
                else if(setLimitPanel.isEnabled()){
                    try {
                            if(Float.parseFloat(limitTextField.getText())>0){
                                savings.setDailyWithdrawalLimit(Float.parseFloat(limitTextField.getText()));
                                JOptionPane.showMessageDialog(null, "New daily withdraw limit aplied.");
                                transaction(savings);//Updates balance panel data
                                setLimitPanel.disable();
                                setLimitPanel.setVisible(false);
                                balancePanel.enable();
                                balancePanel.setVisible(true);
                            }
                            else{
                                JOptionPane.showMessageDialog(null, "Daily withdraw limit should be greater than 0.");
                            }
                        } catch (Exception ex) {
                            JOptionPane.showMessageDialog(null, "Please, enter a valid limit value.");
                    }
                    limitTextField.setText("");
                }
                
        }
        
    }//GEN-LAST:event_enterButtonActionPerformed

